import UIKit

extension CGFloat {
    /// The width of the main screen.
    static var screenWidth: CGFloat {
        UIScreen.main.bounds.width
    }

    /// The height of the main screen.
    static var screenHeight: CGFloat {
        UIScreen.main.bounds.height
    }
}
